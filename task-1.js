// Take four parameters. Multiply the four numbers and then return the result

function multiply(num1, num2, num3, num4) {
    const multi = num1 * num2 * num3 * num4;
    return multi; 
}

const num1 = 1;
const num2 = 2;
const num3 = 3;
const num4 = 4;
const multi = multiply(num1, num2, num3, num4);
console.log(multi);